package com.sk.tutorial.base;

public class Color4F {
    public float r;
    public float g;
    public float b;
    public float a;

    public Color4F() {}
    public Color4F(float r, float g, float b, float a) {
        this.r = r;
        this.g = g;
        this.b = b;
        this.a = a;
    }
}
